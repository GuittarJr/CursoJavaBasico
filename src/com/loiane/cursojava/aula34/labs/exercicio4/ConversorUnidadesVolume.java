package com.loiane.cursojava.aula34.labs.exercicio4;

public class ConversorUnidadesVolume {
    public static double conversorLitroPorCentimetro(double litro){
        return (litro * 1000);
    }
    public static double conversorMtCubicoPorlitro(double metro){
        return (metro * 1000);
    }
    public static double conversorMtCubicoPorPesCubico(double metro){
        return (metro * 35.32);
    }
     public static double conversorGalaoPorPolegadas(double galao){
        return (galao * 231);
    }

}
